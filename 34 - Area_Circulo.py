"""
Leia o valor do raio de um círculo e calcule e imprima a área do círculo correspondente.
Area= pi * raio², considere pi = 3,14.
Achei importante calcular a:
Circunferência = 2 * pi * raio
Diâmetro = 2 * raio
"""

pi = 3.14
raio = 5
area = 3.14 * raio ** 2
c = round(2 * pi * raio, 2)
d = 2 * raio


print(f'Se o raio do cículo é {raio}\nA área do  círculo é {area}.\nA circunferência é {c}.\nE o diâmetro é {d}.')
